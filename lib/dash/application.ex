defmodule Dash.Application do
  # See https://hexdocs.pm/elixir/Application.html
  # for more information on OTP Applications
  @moduledoc false

  use Application

  @impl true
  def start(_type, _args) do
    children = [
      DashWeb.Telemetry,
      Dash.Repo,
      # Starts the Oban supervisor when your application boots.
      {Oban, Application.fetch_env!(:dash, Oban)},
      {DNSCluster, query: Application.get_env(:dash, :dns_cluster_query) || :ignore},
      {Phoenix.PubSub, name: Dash.PubSub},
      # Dashboard data cache
      Dash.Dashboards.DataServer,
      # Start to serve requests, typically the last entry
      DashWeb.Endpoint,
      {AshAuthentication.Supervisor, [otp_app: :dash]},
      # Start active pipelines after application boot
      {Task, fn -> Dash.Pipelines.LifecycleManager.start_all_active_pipelines() end}
    ]

    # See https://hexdocs.pm/elixir/Supervisor.html
    # for other strategies and supported options
    opts = [strategy: :one_for_one, name: Dash.Supervisor]
    Supervisor.start_link(children, opts)
  end

  # Tell Phoenix to update the endpoint configuration
  # whenever the application is updated.
  @impl true
  def config_change(changed, _new, removed) do
    DashWeb.Endpoint.config_change(changed, removed)
    :ok
  end
end
